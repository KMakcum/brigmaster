        ?float $sandUnitPrice = null,
        ?float $cementBagWeightKg = null,
        ?float $brickWeightKg = null,
        ?float $brickPricePerUnit = null,
        ?float $cementBagPrice = null,
        ?float $sandPricePerTonne = null,
        ?string $tileTarget = null,
        ?float $tileLengthMm = null,
        ?float $tileWidthMm = null,
        ?float $tileThicknessMm = null,
        ?float $tileJointMm = null,
